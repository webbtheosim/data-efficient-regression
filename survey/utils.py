import pickle

def save_dictionary(dict, loc):
    '''
        Method for saving a dictionary to a specified location.
    '''
    with open(loc, 'wb') as handle:
        pickle.dump(dict, handle, protocol=pickle.HIGHEST_PROTOCOL)

def load_dictionary(loc):
    '''
        Method for loading a dictionary from a specified location.
    '''
    with open(loc, 'rb') as handle:
        return pickle.load(handle)

